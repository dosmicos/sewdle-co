-- Add a normalized municipality column + index for faster fuzzy lookups
ALTER TABLE public.shipping_coverage
  ADD COLUMN IF NOT EXISTS municipality_normalized text;

-- Populate the normalized column (lowercase, no accents)
UPDATE public.shipping_coverage
SET municipality_normalized = lower(
  translate(
    normalize(municipality, NFD),
    'áéíóúàèìòùâêîôûãõäëïöüñ',
    'aeiouaeiouaeiouaoaeiouen'
  )
)
WHERE municipality_normalized IS NULL;

-- Create index on normalized municipality for fast lookups
CREATE INDEX IF NOT EXISTS idx_shipping_coverage_municipality_norm
  ON public.shipping_coverage(organization_id, municipality_normalized);

-- Create trigger to auto-populate municipality_normalized on insert/update
CREATE OR REPLACE FUNCTION normalize_municipality_trigger()
RETURNS trigger AS $$
BEGIN
  NEW.municipality_normalized := lower(
    translate(
      normalize(NEW.municipality, NFD),
      'áéíóúàèìòùâêîôûãõäëïöüñ',
      'aeiouaeiouaeiouaoaeiouen'
    )
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_normalize_municipality ON public.shipping_coverage;
CREATE TRIGGER trg_normalize_municipality
  BEFORE INSERT OR UPDATE OF municipality ON public.shipping_coverage
  FOR EACH ROW EXECUTE FUNCTION normalize_municipality_trigger();
